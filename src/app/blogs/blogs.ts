export class Blogs {
    header: string;
    calendar: string;
    long: string;
    paragraph: string;
    description: string;
    diet: string;
    vegan: string;
    invite: string;
    nutrition: string;
    meat: string;
    weight: string;

    constructor(header: string, calendar: string, long: string, paragraph: string, description: string, diet: string, vegan: string, invite: string, nutrition: string, meat: string, weight: string) {
       this.header = header;
       this.calendar = calendar;
       this.long = long;
       this.paragraph = paragraph;
       this.description = description;
       this.diet = diet;
       this.vegan = vegan;
       this.invite = invite;
       this.nutrition = nutrition;
       this.meat = meat;
       this.weight = weight;
    }
}
